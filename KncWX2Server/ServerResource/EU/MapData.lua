----------------------------------------------
-- 코보 익스프레스 VIP 티켓 비용 계산
----------------------------------------------
function ComputeCOBOExpressTicketCost( iSrcMapID, iDesMapID, ucLevel )

	local A = 6000
	local B = 0.6
	local d = 0
	local iSrcID = CheckCOBOExpressTicketMapID( iSrcMapID )	
	local iDesID = CheckCOBOExpressTicketMapID( iDesMapID )
	
	if ( (iSrcID == 0) or (iDesID == 0) ) then
		d = math.abs( iDesMapID - iSrcMapID )
	else
		d = math.abs( iDesID - iSrcID )
	end
	
	return ( ucLevel / 60.0 ) * ( A + A * B * ( d - 1 ) )
end
